<h1>Fungsi Irigasi</h1>

<h2>Agar tanaman bisa tumbuh dengan baik, tanah tempat ditanamnya tumbuhan tersebut sangat memerlukan air sebagai elemen penyubur.</h2>

<img src="<?php echo base_url('assets/img/irigasi.jpg') ?>"/>
<p>Air menjadi penentu utama dalam proses-proses yang menciptakan kesuburan tanah ataupun mendorong degradasi tanah. Sistem pengairan tanah pada lahan pertanian itu sendiri biasa kita sebut dengan irigasi, yang berarti mengalirkan air secara buatan dari sumber air kepada lahan- lahan untuk memenuhi kebutuhan tanaman.

Tujuan irigasi adalah mengalirkan air secara teratur sesuai kebutuhan tanaman pada saat persediaan tanah tidak mencukupi untuk mendukung pertumbuhan tanaman. Dengan bantuan irigasi, tanaman pun kemudian bisa tumbuh secara normal. Pemberian air irigasi yang efisien selain dipengaruhi oleh tata cara aplikasi, juga ditentukan oleh kebutuhan air guna mencapai kondisi air tersedia yang dibutuhkan oleh tanaman.</p>
<p>
Secara garis besar, fungsi irigasi untuk lahan pertanian antara lain :
</p>
<p>
1. memasok kebutuhan air bagi tanaman
</p>
<p>
2. menjamin ketersediaan air apabila terjadi betatan (dry spell)
</p>
<p>
3. menurunkan suhu tanah
</p>
<P>
4. mengurangi kerusakan akibat frost (suhu dingin ekstrem)
</P>
<p>
5. melunakkan lapis keras pada saat pengolahan tanah
</p>

<p>Itu tadi penjelasan singkat mengenai fungsi irigasi pada lahan pertanian. Semoga artikel pertanian kali ini bisa sedikit membuka wawasan Anda perihal pentingnya irigasi dalam kesuburan tanaman.</p>
